    private void jTextField_balanceKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_balanceKeyPressed
        int key = evt.getKeyCode();
        if((key >= evt.VK_0&&key<=evt.VK_9) || (key>=evt.VK_NUMPAD0 && key<= evt.VK_NUMPAD9) || key==KeyEvent.VK_BACKSPACE)
        {
            jTextField_balance.setEditable(true);
            jTextField_balance.setBackground(Color.yellow);
            jLabel_status.setText("");
        }
        else
        {
           jTextField_balance.setEditable(false);
            jTextField_balance.setBackground(Color.RED);
            jLabel_status.setForeground(Color.red);
            jLabel_status.setText("Please Enter Only Numeric Value");
        }
    }//GEN-LAST:event_jTextField_balanceKeyPressed

