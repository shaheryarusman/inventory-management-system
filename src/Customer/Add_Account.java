    private void jButton_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddActionPerformed
       
        String check = (String)jTextField_customer_name.getText();
        String  check_bln = jTextField_balance.getText();
           jTextField_customer_name.setBackground(null);
        jTextField_balance.setBackground(null);
        
        if((check.equalsIgnoreCase("")) || check_bln.equalsIgnoreCase("")|| check_bln.equalsIgnoreCase("0"))
        {
            
            if((check.equalsIgnoreCase("")) && (check_bln.equalsIgnoreCase("")|| check_bln.equalsIgnoreCase("0")))
            {
                jTextField_customer_name.setBackground(Color.red);
                jTextField_balance.setBackground(Color.red);
                JOptionPane.showMessageDialog(null,"Please Enter Complete Customer Info" ,"New Customer" , 2);
            }
                else
            {
                           if(check.equalsIgnoreCase(""))
                        {
                       jTextField_customer_name.setBackground(Color.red);
                        jLabel_status.setForeground(Color.red);
                       // jLabel_status.setText("Please Enter Complete Information");
                        JOptionPane.showMessageDialog(null,"Please Enter Customer Name" ,"New Customer" , 1);
                                                  
                        }
                        if(check_bln.equalsIgnoreCase("") || check_bln.equalsIgnoreCase("0"))
                        {
                        jTextField_balance.setBackground(Color.red);
                        jLabel_status.setForeground(Color.red);
                          JOptionPane.showMessageDialog(null,"Please Enter Customer Balance" ,"New Customer" , 1);
                        //jLabel_status.setText("Please Enter Complete Information");
                        }
                        }
        }
        
        
        
        else{
            
             int option = JOptionPane.showConfirmDialog(null, "Do you Want to Add New Customer..??", "Add Customer", JOptionPane.YES_NO_OPTION , 0);
    
            if(option == 0 )

            {
                
        Account_bal ob = new Account_bal();
       int row = 0;
       int balance = (Integer.parseInt(jTextField_balance.getText()));
    
        try {
           row = ob.AddAccount(jTextField_customer_name.getText(), balance);       
           if(row != 0)
        {
          jLabel_status.setForeground(Color.GREEN);
          //jLabel_status.setText("Account is Add");  
          JOptionPane.showMessageDialog(null,"New Customer is Successfully Added" ,"New Customer" , 1);
          jButton_Add.setEnabled(false);
          jButton1_reeset.setEnabled(false);
          jButton1.setEnabled(true);
        }
        else
        {
            jLabel_status.setForeground(Color.RED);
           jLabel_status.setText("Product is not Add"); 
        }
        } catch (SQLException ex) {
            Logger.getLogger(Add_product.class.getName()).log(Level.SEVERE, null, ex);
        }
                  }
        }
    }//GEN-LAST:event_jButton_AddActionPerformed

